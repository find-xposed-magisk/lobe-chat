import { CopyOutlined, EyeInvisibleOutlined, EyeOutlined } from '@ant-design/icons';
import { Button } from '@lobehub/ui/base-ui';
import { App, Flex } from 'antd';
import { type FC } from 'react';
import { useState } from 'react';
import { useTranslation } from 'react-i18next';

interface ApiKeyDisplayProps {
  apiKey?: string;
}

const ApiKeyDisplay: FC<ApiKeyDisplayProps> = ({ apiKey }) => {
  const { t } = useTranslation('auth');
  const [isVisible, setIsVisible] = useState(false);
  const { message } = App.useApp();

  const toggleVisibility = () => {
    setIsVisible(!isVisible);
  };

  const handleCopy = async () => {
    if (!apiKey) return;

    try {
      await navigator.clipboard.writeText(apiKey);
      message.success(t('apikey.display.copySuccess'));
    } catch {
      message.error(t('apikey.display.copyError'));
    }
  };

  // Mask with the key's real prefix (e.g. `sk-lh-`) so the hidden state
  // doesn't misrepresent the key format.
  const maskPrefixLength = apiKey ? apiKey.lastIndexOf('-') + 1 : 0;
  const displayValue =
    apiKey &&
    (isVisible
      ? apiKey
      : `${apiKey.slice(0, maskPrefixLength)}${'*'.repeat(apiKey.length - maskPrefixLength)}`);

  if (!apiKey) {
    return t('apikey.display.autoGenerated');
  }

  return (
    <Flex align="center" gap={8}>
      <span style={{ fontSize: '14px' }}>{displayValue}</span>
      <Flex>
        <Button
          icon={isVisible ? <EyeInvisibleOutlined /> : <EyeOutlined />}
          size="small"
          title={isVisible ? t('apikey.display.hide') : t('apikey.display.show')}
          type="text"
          onClick={toggleVisibility}
        />
        <Button
          icon={<CopyOutlined />}
          size="small"
          title={t('apikey.display.copy')}
          type="text"
          onClick={handleCopy}
        />
      </Flex>
    </Flex>
  );
};

export default ApiKeyDisplay;
